import random
a = random.randint(100000, 1000000)
print(f"Случайный номер билета: {a}")
b = a // 1000 
c = a % 1000  

f = (b // 100) + (b % 100 // 10) + (b % 10)
s = (c // 100) + (c % 100 // 10) + (c % 10)

if f == s:
    print(f"Ваш билет {a} - счастливый! Поздравляю!")
else:
    print(f"Ваш билет {a} - не счастливый! Повезет в другой раз!")